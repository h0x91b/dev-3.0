/**
 * Semantic-token styling per priority level. NO hardcoded colors — only theme
 * tokens, so both dark and light themes follow automatically (DESIGN.md /
 * AGENTS.md). Design intent: a "heat ramp" by importance — P0 red → P1 orange →
 * P2 yellow → P3 green cool down as urgency drops, and only the lowest level
 * (P4) is chromatically neutral (grey). Built entirely from existing semantic
 * tokens (danger / awake / warning / success / fg-muted) so no palette additions
 * are needed.
 */
export interface PriorityStyle {
	/** Vivid look for the always-on `P{n}` badge (card, picker, detail/create modals). */
	badge: string;
	/** Unselected filter chip: the level's own colour, dimmed — lifts on hover. */
	chipIdle: string;
	/** Selected filter chip: colour lit to full with a matching ring highlight. */
	chipActive: string;
}

